// common/middleware/auth.middleware.ts - Middleware барои санҷиши токен
import { Request, Response, NextFunction } from 'express';
import { verifyToken } from '../utils/token';

export const authMiddleware = (req: Request, res: Response, next: NextFunction): void => {
    const authHeader = req.headers.authorization;
    if (!authHeader || !authHeader.startsWith('Bearer ')) {
        res.status(401).json({ success: false, message: 'Токен нест ё нодуруст аст' });
        return;
    }

    const token = authHeader.split(' ')[1];
    const payload = verifyToken(token);
    if (!payload) {
        res.status(401).json({ success: false, message: 'Токен беэътибор ё бекор шудааст' });
        return;
    }

    (req as any).user = payload;
    next();
};
